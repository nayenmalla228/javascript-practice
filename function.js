//addition using function
function add(a, b) {
  return a + b;
}
const sum = add(10, 19);
console.log(sum);

//substraction using function

function sub(c, d) {
  return c - d;
}
const subs = sub(10, 2);
console.log(subs);

const multiply = (a, b) => {  //arrow function
  return a * b;
};
const product = multiply(5, 2);
console.log(product);

const division =(a,b)=>a/b;
console.log(division(50,25));